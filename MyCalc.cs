namespace MyCalcLib
{
    public class MyCalc
    {
        public int Sum(int x, int y) 
        { 
            return x + y; 
        }

        public int Multiplication(int xX, int yY) 
        {
            return xX * yY;
        }

        public int Divide(int Xx, int Yy) 
        { 
            return Xx / Yy;
        }

        public int Subtract(int Xx, int Yy)
        {
            return Xx - Yy;
        }
    }
}
